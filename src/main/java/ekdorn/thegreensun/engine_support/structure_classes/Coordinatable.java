package ekdorn.thegreensun.engine_support.structure_classes;

public class Coordinatable {
    private double xCoord;
    private double yCoord;

    public double getX() {
        return xCoord;
    }

    public void setX(double x) {
        this.xCoord = x;
    }

    public double getY() {
        return yCoord;
    }

    public void setY(double y) {
        this.yCoord = y;
    }
}
